using System.ComponentModel.DataAnnotations;

namespace CDEWeb.Data.Model
{
    public class Empresa
    {
        //-------------------------------------------------------------
        #region Variáveis
        //-------------------------------------------------------------
        [Key]
        public int      Id      { get; set; }
        
        public string   Name    { get; set; }
        //-------------------------------------------------------------
        #endregion Variáveis
        //-------------------------------------------------------------

        //-------------------------------------------------------------
        #region Construtor
        //-------------------------------------------------------------
        public Empresa() 
        {
            Name = string.Empty;
        }
        //-------------------------------------------------------------
        #endregion Construtor
        //-------------------------------------------------------------
    }
}
